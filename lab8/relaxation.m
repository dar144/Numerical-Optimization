function u_one = relaxation(D, delta_t, delta, j, i, u0, u1, vx, vy, c)
    delta2 = 2*delta;
    u_one = c*(u0(j,i)-delta_t/2*vx(j,i)*((u0(j,i+1)-u0(j,i-1))/delta2+...
        (u1(j,i+1)-u1(j,i-1))/delta2) - delta_t/2*vy(j,i)*((u0(j+1,i)-u0(j-1,i))/delta2+...
        (u1(j+1,i)-u1(j-1,i))/delta2) + delta_t/2*D*((u0(j,i+1)+u0(j,i-1)+u0(j+1,i)+u0(j-1,i)-4*u0(j,i))/delta^2+...
        (u1(j,i+1)+u1(j,i-1)+u1(j+1,i)+u1(j-1,i))/delta^2)); 
end